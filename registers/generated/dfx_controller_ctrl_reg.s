# --- DFX_CTRL

.equ DFX_CTRL_BASE_ADDR, 0x10000400

# CONTROL - Control register.
.equ DFX_CTRL_CONTROL_ADDR, 0x0

# CONTROL.CMD - Command.
.equ DFX_CTRL_CONTROL_CMD_WIDTH 8
.equ DFX_CTRL_CONTROL_CMD_LSB 0
.equ DFX_CTRL_CONTROL_CMD_MASK 0xff
# CONTROL.BYTE - Byte field containing extra info.
.equ DFX_CTRL_CONTROL_BYTE_WIDTH 8
.equ DFX_CTRL_CONTROL_BYTE_LSB 8
.equ DFX_CTRL_CONTROL_BYTE_MASK 0xff00
# CONTROL.HALFWORD - Halfword field containing extra info.
.equ DFX_CTRL_CONTROL_HALFWORD_WIDTH 16
.equ DFX_CTRL_CONTROL_HALFWORD_LSB 16
.equ DFX_CTRL_CONTROL_HALFWORD_MASK 0xffff0000
