# --- RESET

.equ RESET_BASE_ADDR, 0x100000d0

# CTRL - Reset control register
.equ RESET_CTRL_ADDR, 0x0

# CTRL.NDM_RESET - Non-debug module reset
.equ RESET_CTRL_NDM_RESET_WIDTH 1
.equ RESET_CTRL_NDM_RESET_LSB 0
.equ RESET_CTRL_NDM_RESET_MASK 0x1
# CTRL.DM_RESET - Debug module reset
.equ RESET_CTRL_DM_RESET_WIDTH 1
.equ RESET_CTRL_DM_RESET_LSB 1
.equ RESET_CTRL_DM_RESET_MASK 0x2
# CTRL.USB_RESET - USB module reset
.equ RESET_CTRL_USB_RESET_WIDTH 1
.equ RESET_CTRL_USB_RESET_LSB 2
.equ RESET_CTRL_USB_RESET_MASK 0x4
# REASON - Reset reason register
.equ RESET_REASON_ADDR, 0x4

# REASON.POR - Power-on reset
.equ RESET_REASON_POR_WIDTH 1
.equ RESET_REASON_POR_LSB 0
.equ RESET_REASON_POR_MASK 0x1
# REASON.SW_NDM - Software triggered NDM reset
.equ RESET_REASON_SW_NDM_WIDTH 1
.equ RESET_REASON_SW_NDM_LSB 1
.equ RESET_REASON_SW_NDM_MASK 0x2
# REASON.SW_DM - Software triggered DM reset
.equ RESET_REASON_SW_DM_WIDTH 1
.equ RESET_REASON_SW_DM_LSB 2
.equ RESET_REASON_SW_DM_MASK 0x4
# REASON.NDM - Non-debug module reset
.equ RESET_REASON_NDM_WIDTH 1
.equ RESET_REASON_NDM_LSB 3
.equ RESET_REASON_NDM_MASK 0x8
# REASON.EXT - External reset
.equ RESET_REASON_EXT_WIDTH 1
.equ RESET_REASON_EXT_LSB 4
.equ RESET_REASON_EXT_MASK 0x10
# REASON.SW_USB - Software triggered USB reset
.equ RESET_REASON_SW_USB_WIDTH 1
.equ RESET_REASON_SW_USB_LSB 5
.equ RESET_REASON_SW_USB_MASK 0x20
