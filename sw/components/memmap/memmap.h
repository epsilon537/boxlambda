#ifndef MEMMAP_H
#define MEMMAP_H

#define IMEM_BASE (0U)
#define IMEM_SIZE_BYTES (256*1024U)
#define BOOTLOADER_IMG_SIZE_BYTES 0x100000
#define APP_SW_IMG_SIZE_BYTES 0x200000

#define SPIFLASH_BASE (0x11000000U)
#define SPIFLASH_SIZE_BYTES (16*1024*1024U)
#define SPIFLASH_BITSTREAM_BASE SPIFLASH_BASE
#define SPIFLASH_BITSTREAM_SIZE_BYTES 0x400000
#define SPIFLASH_BOOTLOADER_BASE (SPIFLASH_BASE + 0x500000) //Note the 0x100000 gap from the end of the bitstream.
#define SPIFLASH_BOOTLOADER_SIZE_BYTES BOOTLOADER_IMG_SIZE_BYTES
#define SPIFLASH_SW_BASE (SPIFLASH_BOOTLOADER_BASE + SPIFLASH_BOOTLOADER_SIZE_BYTES)
#define SPIFLASH_SW_SIZE_BYTES APP_SW_IMG_SIZE_BYTES
#define SPIFLASH_NVDATA_BASE (SPIFLASH_SW_BASE + SPIFLASH_SW_SIZE_BYTES)
#define SPIFLASH_NVDATA_SIZE_BYTES (4*1024*1024)

#define VERA_VRAM_BASE (0x12040000U)
#define VERA_VRAM_SIZE_BYTES (128*1024U)
#define VERA_CAPTURE_RAM_BASE (0x12003000U)
#define VERA_CAPTURE_RAM_SIZE_BYTES (640U*4)
#define VERA_PALETTE_RAM_BASE (0x12002000U)
#define VERA_PALETTE_RAM_SIZE_BYTES (512U)
#define VERA_SPRITE_RAM_BASE (0x12001000U)
#define VERA_SPRITE_RAM_SIZE_BYTES (1024U)

#define SDRAM_BASE (0x20000000U)
#define SDRAM_SIZE_BYTES (256*1024*1024U)

#endif /*MEMMAP_H*/
