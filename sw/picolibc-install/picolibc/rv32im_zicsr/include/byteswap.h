#ifndef __BYTESWAP_H__
#define __BYTESWAP_H__

#ifdef __GNUC__
#define	bswap_16(_x)	__builtin_bswap16(_x)
#define	bswap_32(_x)	__builtin_bswap32(_x)
#define	bswap_64(_x)	__builtin_bswap64(_x)
#else /* __GNUC__ */
static __inline __uint16_t
bswap_16(__uint16_t _x)
{

	return ((__uint16_t)((_x >> 8) | ((_x << 8) & 0xff00)));
}

static __inline __uint32_t
bswap_32(__uint32_t _x)
{

	return ((__uint32_t)((_x >> 24) | ((_x >> 8) & 0xff00) |
	    ((_x << 8) & 0xff0000) | ((_x << 24) & 0xff000000)));
}

static __inline __uint64_t
bswap_64(__uint64_t _x)
{

	return ((__uint64_t)((_x >> 56) | ((_x >> 40) & 0xff00) |
	    ((_x >> 24) & 0xff0000) | ((_x >> 8) & 0xff000000) |
	    ((_x << 8) & ((__uint64_t)0xff << 32)) |
	    ((_x << 24) & ((__uint64_t)0xff << 40)) |
	    ((_x << 40) & ((__uint64_t)0xff << 48)) | ((_x << 56))));
}
#endif /* !__GNUC__ */

#endif /* __BYTESWAP_H__ */
